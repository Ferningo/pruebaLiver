package com.prueba.jose.demo.model;

public class Folio {

    private String date;

    private int folioNumber;

    private String concatValue;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getFolioNumber() {
        return folioNumber;
    }

    public void setFolioNumber(int folioNumber) {
        this.folioNumber = folioNumber;
    }

    public String getConcatValue() {
        return concatValue;
    }

    public void setConcatValue(String concatValue) {
        this.concatValue = concatValue;
    }
}
